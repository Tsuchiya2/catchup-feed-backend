func newLearningRepo(t *testing.T) (repository.LearningRepository, sqlmock.Sqlmock, func()) {
	t.Helper()
	db, mock, err := sqlmock.New()
	require.NoError(t, err)
	return postgres.NewLearningRepo(db), mock, func() { _ = db.Close() }
}

func testDay(y int, m time.Month, d int) time.Time {
	return time.Date(y, m, d, 0, 0, 0, 0, time.UTC)
}

var learningItemCols = []string{
	"id", "kind", "article_id", "book_id", "concept", "question", "answer",
	"provider", "stage", "due_on", "retired_at", "created_at",
}

func TestLearningRepo_InsertItem(t *testing.T) {
	repo, mock, closeFn := newLearningRepo(t)
	defer closeFn()

	articleID := int64(42)
	item := learning.NewItem{
		Kind:      learning.KindArticle,
		ArticleID: &articleID,
		Concept:   "goroutine leak",
		Question:  "Q",
		Answer:    "A",
		Provider:  "gemini",
	}
	// stage は 0 固定、due_on はテキスト + ::date で渡す(§12-10)。
	mock.ExpectQuery(regexp.QuoteMeta("INSERT INTO learning_items")).
		WithArgs(learning.KindArticle, &articleID, nil, "goroutine leak", "Q", "A", "gemini", "2026-07-08").
		WillReturnRows(sqlmock.NewRows([]string{"id"}).AddRow(int64(5)))

	id, err := repo.InsertItem(context.Background(), item, testDay(2026, 7, 8))
	require.NoError(t, err)
	assert.Equal(t, int64(5), id)
	assert.NoError(t, mock.ExpectationsWereMet())
}

// TestLearningRepo_InsertItem_BookProviderEnforced pins §12-4: the
// application layer rejects a kind='book' item generated by a cloud
// provider before any SQL is issued (C-12 — 書籍はローカル LLM 限定).
func TestLearningRepo_InsertItem_BookProviderEnforced(t *testing.T) {
	repo, mock, closeFn := newLearningRepo(t)
	defer closeFn()

	bookID := int64(7)
	item := learning.NewItem{
		Kind:    learning.KindBook,
		BookID:  &bookID,
		Concept: "c", Question: "q", Answer: "a",
		Provider: "gemini",
	}
	_, err := repo.InsertItem(context.Background(), item, testDay(2026, 7, 8))
	require.Error(t, err)
	assert.Contains(t, err.Error(), "ollama")
	assert.NoError(t, mock.ExpectationsWereMet(), "no SQL may be issued for an invalid item")
}

// TestLearningRepo_HasArticleItemCreatedOn pins the same-day dedupe query
// shape (§12-2): kind='article' only, and the JST day boundaries derived
// in SQL via AT TIME ZONE (created_at は UTC の timestamptz、§12-10).
func TestLearningRepo_HasArticleItemCreatedOn(t *testing.T) {
	repo, mock, closeFn := newLearningRepo(t)
	defer closeFn()

	mock.ExpectQuery(
		regexp.QuoteMeta("created_at >= ($2::date)::timestamp AT TIME ZONE 'Asia/Tokyo'")+
			`[\s\S]*`+regexp.QuoteMeta("created_at <  ($2::date + 1)::timestamp AT TIME ZONE 'Asia/Tokyo'")).
		WithArgs(learning.KindArticle, "2026-07-05").
		WillReturnRows(sqlmock.NewRows([]string{"exists"}).AddRow(true))

	exists, err := repo.HasArticleItemCreatedOn(context.Background(), testDay(2026, 7, 5))
	require.NoError(t, err)
	assert.True(t, exists)
	assert.NoError(t, mock.ExpectationsWereMet())
}

func TestLearningRepo_ListDue(t *testing.T) {
	repo, mock, closeFn := newLearningRepo(t)
	defer closeFn()

	articleID := int64(42)
	created := time.Date(2026, 7, 1, 0, 0, 0, 0, time.UTC)
	// §6.3 親裁定: 前日以前の未採点ログ(rl.asked_on < day)を持つ項目は
	// 選定から除外。同日ログは除外しない = 同日 rev の冪等を保つ。
	mock.ExpectQuery(
		regexp.QuoteMeta("WHERE retired_at IS NULL AND due_on <= $1::date")+
			`[\s\S]*`+regexp.QuoteMeta("AND rl.result IS NULL")+
			`[\s\S]*`+regexp.QuoteMeta("AND rl.asked_on < $1::date")).
		WithArgs("2026-07-07", 4).
		WillReturnRows(sqlmock.NewRows(learningItemCols).
			AddRow(int64(1), "article", &articleID, nil, "c1", "q1", "a1", "gemini", 0, testDay(2026, 7, 6), nil, created).
			AddRow(int64(2), "book", nil, int64(7), "c2", "q2", "a2", "ollama", 1, testDay(2026, 7, 7), nil, created))

	items, err := repo.ListDue(context.Background(), testDay(2026, 7, 7), 4)
	require.NoError(t, err)
	require.Len(t, items, 2)
	assert.Equal(t, int64(1), items[0].ID)
	assert.Equal(t, learning.KindArticle, items[0].Kind)
	assert.Equal(t, articleID, *items[0].ArticleID)
	assert.Equal(t, learning.KindBook, items[1].Kind)
	assert.Equal(t, 1, items[1].Stage)
	assert.True(t, items[1].DueOn.Equal(testDay(2026, 7, 7)))
	assert.NoError(t, mock.ExpectationsWereMet())
}

